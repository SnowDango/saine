import checker from "license-checker"
import { writeFileSync, readFileSync } from "fs"
import { resolve, dirname } from "path"
import { fileURLToPath } from "url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const rootDir = resolve(__dirname, "..")

const pkg = JSON.parse(readFileSync(resolve(rootDir, "package.json"), "utf-8"))
const prodDeps = new Set(Object.keys(pkg.dependencies || {}))

checker.init({ start: rootDir }, (err, packages) => {
  if (err) {
    console.error(err)
    process.exit(1)
  }

  const licenses = Object.entries(packages)
    .filter(([name]) => {
      const pkgName = name.replace(/@[^@]*$/, "") // remove version suffix
      return prodDeps.has(pkgName)
    })
    .map(([name, info]) => ({
      name,
      license: info.licenses || "Unknown",
      repository: info.repository || "",
      publisher: info.publisher || "",
    }))

  const output = `// Auto-generated by tools/generate-licenses.mjs
export const thirdPartyLicenses = ${JSON.stringify(licenses, null, 2)} as const;
`

  writeFileSync(resolve(rootDir, "tabs", "third-party-licenses.ts"), output)
  console.log(`Generated ${licenses.length} license entries.`)
})
